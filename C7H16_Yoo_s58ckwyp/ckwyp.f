      SUBROUTINE CKWYP  (P, T, YISO, ICKWRK, RCKWRK, WISO)
      IMPLICIT DOUBLE PRECISION (A-H, O-Z), INTEGER (I-N)
      DIMENSION ICKWRK(*), RCKWRK(*)
      DIMENSION YISO(*), Y(64), WISO(*), WDOT(64)
      DIMENSION RF(387),RB(387),RKLOW(12),C(64),XQ(24)
C
      CALL XISO(YISO, Y)
      CALL YTCP(P, T, Y, C)
      CALL RATT(T, RF, RB, RKLOW)
